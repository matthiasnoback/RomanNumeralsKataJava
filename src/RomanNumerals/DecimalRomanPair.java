package RomanNumerals;

public class DecimalRomanPair {
    private int decimal;
    private String roman;

    public int getDecimal() {
        return decimal;
    }

    public String getRoman() {
        return roman;
    }

    public DecimalRomanPair(int decimal, String roman) {

        this.decimal = decimal;
        this.roman = roman;
    }
}
